#include <stdio.h>
int main(void)
{
    printf("The condition 1>5 is %d \n", 1 > 5);
    printf("The condition 1<5 is %d \n", 1 < 5);
    printf("The condition 1>=5 is %d \n", 1 >= 5);
    printf("The condition 1<=5 is %d \n", 1 <= 5);
    printf("The condition 1==5 is %d \n", 1 == 5);
    printf("The condition 1!=5 is %d \n", 1 != 5);
    // 1 is true
    // 0 is false
    return 0;

}
